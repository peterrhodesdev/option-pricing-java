package dev.peterrhodes.optionpricing.enums;

/**
 * Bracket types used in LaTex equations.
 */
public enum BracketType {

    /**
     * No brackets.
     */
    NONE,

    /**
     * Round brackets (parentheses), e.g.&nbsp;(x + y).
     */
    ROUND,

    /**
     * Square brackets, e.g.&nbsp;[x + y].
     */
    SQUARE,

    /**
     * Curly brackets, e.g.&nbsp;{x + y}.
     */
    CURLY
}
