/**
 * Types of precision for displaying numbers.
 */
public enum PrecisionType {

    /**
     * To a number of decimal places.
     */
    DECIMAL_PLACES,

    /**
     * To a number of significant figures.
     */
    SIGNIFICANT_FIGURES,

    /**
     * Leave the number unchanged.
     */
    UNCHANGED
}
